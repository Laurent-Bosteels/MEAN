export class Friend {
    id : string;
    fname : string;
    lname : string;
    email : string;
    phone : string;
    language : string;

    constructor(id: string, fname: string, lname: string, email:string, phone:string, language:string) {
      this.id = id;
      this.fname = fname;
      this.lname = lname;
      this.email = email;
      this.phone = phone;
      this.language = language;

    }

}
